    def statistics
      "Number of items: #{TO_ASSIGN.size} (successful or not)<br><br>Appears more than once: #{appears_more_than_once}".html_safe
    end

    def render
      as_table do |t|
        t.header :subgenus_name, :target_subgenus, :subgenus_status, :species_name, :target_species, :species_status, :current_subgenus,
          :cannot_assign_because, :appears_more_than_once?
        t.rows(TO_ASSIGN) do |(subgenus_part_of_name, species_epithet)|
          subgenus_name_string = "Camponotus (#{subgenus_part_of_name})"
          subgenus_candidates = Subgenus.where(name_cache: subgenus_name_string)
          subgenus_candidates_count = subgenus_candidates.count
          subgenus = subgenus_candidates.first

          species_name_string = "Camponotus #{species_epithet}"
          species_candidates = Species.where(name_cache: species_name_string)
          species_candidates_count = species_candidates.count
          species = species_candidates.first

          cannot_assign_because = []
          cannot_assign_because << "Found no subgenus with the name #{subgenus_name_string}" unless subgenus
          cannot_assign_because << "Found more than one subgenus: #{link_taxa(subgenus_candidates)}" if subgenus_candidates_count > 1
          cannot_assign_because << "Found no species with the name #{species_name_string}" unless species
          cannot_assign_because << "Found more than one species: #{link_taxa(species_candidates)}" if species_candidates_count > 1
          if species&.subgenus
            if subgenus_candidates_count == 1 && species.subgenus == subgenus
               cannot_assign_because << "Already done!"
            else
               cannot_assign_because << "Species is already assigned to a different subgenus"
            end
          end
          cannot_assign_because = ["Target subgenus is 'unassigned'"] if subgenus_part_of_name == "unassigned" # Overrides other reasons.

          [
            subgenus_name_string,
            (subgenus.link_to_taxon if subgenus_candidates_count == 1),
            (subgenus.status if subgenus_candidates_count == 1),

            species_name_string,
            (species.link_to_taxon if species_candidates_count == 1),
            (species.status if species_candidates_count == 1),

            (species.subgenus.link_to_taxon if species&.subgenus),
            cannot_assign_because.join("<br><br>"),
            ('Yes' if species_epithet.in?(appears_more_than_once))
          ]
        end
      end
    end

    private

      def appears_more_than_once
        @appears_more_than_once ||= TO_ASSIGN.map(&:second).group_by { |e| e }.select { |k, v| v.size > 1 }.map(&:first)
      end

      def link_taxa taxa
        taxa.map(&:link_to_taxon).join(", ")
      end
  end
